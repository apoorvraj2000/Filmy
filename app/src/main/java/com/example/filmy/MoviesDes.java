package com.example.filmy;

public class MoviesDes {
    String title;
    String date;
    String rating;
    String image;
    String popularity;
    String overview;
    int id;
    public MoviesDes(String title, String date, String rating, String image, String popularity, String overview, int id) {
        this.title = title;
        this.date = date;
        this.rating = rating;
        this.image = image;
        this.popularity = popularity;
        this.overview = overview;
        this.id=id;
    }
}
